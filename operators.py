# Let's see the operators in action

### Intro to Data types & Operators
# - ` +  -  *  /`

a = 24
b = 16

print(a + b)
print(a - b)
print(a * b)
print(a / b)

###### Comparison operators
# - `>` greater than
# - `<` less than
# - `==` true or false
# - `>=` greater than or equal to
# - `<=` less than or equal to

print(a > b)
print(a < b)
print(a == b)
print(a >= b)

